     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $contracts = new Contract();
        $contracts->subject = $request->input('subject');
        $contracts->fullname = $request->input('fullname');
        $contracts->email = $request->input('email');
        $contracts->phonenumber = $request->input('phonenumber');
        $contracts->message = $request->input('message');
        $contracts->save();

        return response()->json(['data' => $contracts]);
    }

